#short

def say_hello(name):
    print("Hallo " + name)
    print("Willkommen zurück")

say_hello("Mike")
say_hello("Vincent")
say_hello("Marco")
print("Das war die 'short' Option 'ENDE' ")

#Vor- Nachname

def say_hello(first_name, last_name):
    print("Hallo " + first_name + " " + last_name)
    print("Willkommen zurück")

say_hello("Mike" , "Apexmann")
say_hello("Vincent", "noob")
say_hello("Marco" , "WoWguy")
print("Das war die 'short' Option 'ENDE' ")


