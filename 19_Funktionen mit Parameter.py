# Long
def say_hello1():
    print("Hallo Mike")
    print("Willkommen zurück")


def say_hello2():
    print("Hallo Vincent")
    print("Willkommen zurück")

def say_hello3():
    print("Hallo Marco")
    print("Willkommen zurück")

say_hello1()
say_hello2()
say_hello3()
